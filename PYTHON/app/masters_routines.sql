CREATE DEFINER=`root`@`%` PROCEDURE `sp_update_avg_transmission_loss`()
BEGIN
    DECLARE avg_loss FLOAT;

    SELECT AVG(loss_percentage)
    INTO avg_loss
    FROM transmission_loss_master;

    -- If the table is empty, insert a new row
    IF (SELECT COUNT(*) FROM configuration) = 0 THEN
        INSERT INTO configuration (average_transmission_loss_percent) VALUES (avg_loss);
    ELSE
        -- Otherwise update the existing row
        UPDATE configuration
        SET average_transmission_loss_percent = avg_loss;
    END IF;
   
END ;;
