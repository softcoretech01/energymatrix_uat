CREATE DEFINER=`root`@`%` PROCEDURE `sp_initialize_windmill_schema`()
BEGIN
    DECLARE has_ae_name INT DEFAULT 0;
    DECLARE has_am_name INT DEFAULT 0;
    DECLARE has_contact_number INT DEFAULT 0;

    -- Check for ae_name column
    SELECT COUNT(*) INTO has_ae_name FROM information_schema.COLUMNS 
    WHERE TABLE_SCHEMA = DATABASE() AND TABLE_NAME = 'master_windmill' AND COLUMN_NAME = 'ae_name';

    -- Check for am_name column
    SELECT COUNT(*) INTO has_am_name FROM information_schema.COLUMNS 
    WHERE TABLE_SCHEMA = DATABASE() AND TABLE_NAME = 'master_windmill' AND COLUMN_NAME = 'am_name';

    -- If only am_name exists, rename it to ae_name
    IF has_am_name = 1 AND has_ae_name = 0 THEN
        ALTER TABLE master_windmill CHANGE COLUMN am_name ae_name VARCHAR(100) NULL;
        SET has_ae_name = 1;
        SET has_am_name = 0;
    END IF;

    -- If both exist, migrate data from am_name to ae_name and drop am_name
    IF has_am_name = 1 AND has_ae_name = 1 THEN
        UPDATE master_windmill SET ae_name = am_name 
        WHERE (ae_name IS NULL OR ae_name = '') AND (am_name IS NOT NULL AND am_name <> '');
        ALTER TABLE master_windmill DROP COLUMN am_name;
        SET has_am_name = 0;
    END IF;

    -- Ensure ae_name exists
    IF has_ae_name = 0 THEN
        ALTER TABLE master_windmill ADD COLUMN ae_name VARCHAR(100) NULL;
    END IF;

    -- Check for contact_number column
    SELECT COUNT(*) INTO has_contact_number FROM information_schema.COLUMNS 
    WHERE TABLE_SCHEMA = DATABASE() AND TABLE_NAME = 'master_windmill' AND COLUMN_NAME = 'contact_number';

    IF has_contact_number = 0 THEN
        ALTER TABLE master_windmill ADD COLUMN contact_number VARCHAR(20) NULL;
    END IF;
END ;;
